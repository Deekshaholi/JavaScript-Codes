// short date format "MM/DD/YYYY"
const date = new Date("12/25/2013");
console.log(date); 

// long date format "MMM DD YYYY"
const date1 = new Date("Nov 1 2020");
console.log(date1); 

// month and day can be in any order
const date2 = new Date("1 Jun 2120");
console.log(date2); 


const date3 = new Date("May 1 1999");
console.log(date3); 

const date4 = new Date("JULY, 1, 2020");
console.log(date4);